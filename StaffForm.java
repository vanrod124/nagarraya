    private void jbRemoveBooksActionPerformed(java.awt.event.ActionEvent evt) {
        String title = jtSearchTittle3.getText().trim();
        String author = jtSearchAuthor4.getText().trim();
        String category = jtSearchAuthor5.getText().trim();
        boolean found = false;

        for (int i = 0; i < books.size(); i++) {
            Book book = books.get(i);
            if (book.getTittle().equalsIgnoreCase(title) && book.getAuthor().equalsIgnoreCase(author) && book.getCategory().equalsIgnoreCase(category)) {
                books.remove(i);
                found = true;
                break;
            }
        }

        populateTable1();
        populateTable2();
        dashboard.refreshBooksTable();

        jtSearchTittle3.setText("");
        jtSearchAuthor4.setText("");
        jtSearchAuthor5.setText("");

        if (found) {
            JOptionPane.showMessageDialog(this, "Book removed successfully!");
        } else {
            JOptionPane.showMessageDialog(this, "Book not found!");
        }
    }

