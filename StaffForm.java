public void populateTable1() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); 

    for (BorrowedBook borrowedBook : theLastOne) {
        model.addRow(new Object[]{
            borrowedBook.getTitle(),
            borrowedBook.getAuthor(),
            borrowedBook.getBorrowerName(),
            borrowedBook.getBorrowDate(),
            borrowedBook.getReturnDate()
        });
    }
}

public void populateTable2() {
    DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
    model.setRowCount(0); 

    for (Book book : books) {
        model.addRow(new Object[]{
            book.getTittle(),
            book.getAuthor(),
            book.getCategory()
        });
    }
}


private void updateJTable1WithBorrowedBooks() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // Clear existing rows

    // Assuming theLastOne is the list of borrowed books
    for (BorrowedBook borrowedBook : theLastOne) {
        model.addRow(new Object[]{borrowedBook.getTitle(), borrowedBook.getAuthor(), borrowedBook.getBorrowerName(), borrowedBook.getBorrowDate(), borrowedBook.getReturnDate()});
    }

    // Notify the table of the changes
    model.fireTableDataChanged();
}




